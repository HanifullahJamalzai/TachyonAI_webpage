    {
        $this->middleware(['auth', 'verified']);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('admin.pricing.index')
                    ->with('pricing', Pricing::findOrFail(1))
                    ->with('page', 'Pricing');
    }

